/**
 * @file rule_engine.h
 * @brief 规则引擎 — 安全监测与报警（论文 3.1.3、5.3 节）
 *
 * 【职责】
 *   在 cabin_fsm 负责核心气压控制逻辑的基础上，
 *   rule_engine 负责安全监测和报警：
 *     - 环境参数超阈值 → 执行器动作 + 语音/声光报警 + MQTT 推送
 *     - 传感器/执行器故障 → 停机保护
 *     - 压力变化率异常 → 停机保护
 *
 * 【与 cabin_fsm 的分工】
 *   cabin_fsm:   核心流程控制（升压/保压/泄压/急停/安全模式）
 *   rule_engine: 安全监测（阈值检查 → 报警分发 → 通知 FSM 状态切换）
 *
 * ═══════════════════════════════════════════════════════════
 *  一、氧浓度异常报警（三级，高氧方向）
 * ═══════════════════════════════════════════════════════════
 *   一级预警（22% ≤ O₂ < 25%）：
 *     关闭增氧机与供氧阀，开启进气阀与排气阀进行通风换气，
 *     协调阀门开度使压力波动最小。
 *
 *   二级报警（25% ≤ O₂ < 30%）：
 *     关闭增氧机与供氧阀，全开进气阀与排气阀加大换气，
 *     启动声光报警。
 *
 *   三级故障（O₂ ≥ 30%）：
 *     关闭全部供氧设备，启动紧急泄压，打开排气阀快速降压，
 *     发出紧急报警提示用户立即出舱，
 *     系统进入安全模式，停止所有增压与供氧操作。
 *
 * ═══════════════════════════════════════════════════════════
 *  二、压力异常报警（三级软件 + 一级机械保护）
 * ═══════════════════════════════════════════════════════════
 *   预警（≥ 58 KPa）：
 *     系统发出预警提示，自动停止增压。
 *
 *   超限报警（≥ 60 KPa）：
 *     立即关闭空气泵，打开排气阀强制泄压，进入异常保护状态。
 *
 *   极端超压（≥ 62 KPa）：
 *     进入紧急模式，排气阀全开，快速泄压。
 *
 *   机械安全保护（≥ 70 KPa）：
 *     机械泄压阀自动开启，强制泄压（硬件保护，不经软件）。
 *
 * ═══════════════════════════════════════════════════════════
 *  三、温湿度报警（仅报警，不触发停机/保护动作）
 * ═══════════════════════════════════════════════════════════
 *   温度（两级）：
 *     > 40℃  → 警告（语音 + MQTT）
 *     > 50℃  → 故障（语音 + MQTT + FSM_EVT_FAULT）
 *
 *   湿度（高低两端）：
 *     < 30%RH → 低湿警告（语音 + MQTT）
 *     > 80%RH → 高湿警告（语音 + MQTT）
 *
 * ═══════════════════════════════════════════════════════════
 *  四、其他系统异常报警
 * ═══════════════════════════════════════════════════════════
 *   传感器故障：数据超量程 / 通信超时(连续3次) / 无响应 → 停机保护
 *   执行器故障：状态异常 / 继电器粘连 / 无反馈 → 停机保护
 *   压力变化率过快：升/降压速率 > 15 KPa/min → 停机保护
 *   通信异常：串口屏中断 / 云平台连接超时 → 报警提示（不停机）
 *
 * ═══════════════════════════════════════════════════════════
 *  五、严重异常统一处理流程
 * ═══════════════════════════════════════════════════════════
 *   1. 立即关闭空气泵、增氧机等所有执行器
 *   2. 打开排气阀，启动泄压程序
 *   3. 发出声光报警、双屏提示、语音提醒
 *   4. 记录故障信息并保存
 *   5. 系统进入安全模式，禁止自动重启
 *
 * 【防抖策略】
 *   回差法（hysteresis）：报警触发后设置 active 标志，
 *   需回落到阈值以下一定幅度才解除，避免反复报警。
 *
 * 【规则存储】
 *   保存在 NVS 中（data_store），支持通过串口屏/MQTT 修改阈值。
 *   上电时从 NVS 加载，修改后立即保存。
 */
#ifndef RULE_ENGINE_H
#define RULE_ENGINE_H

#include "esp_err.h"
#include <stdint.h>
#include <time.h>

/* ════════════════════════════════════════════════════════
 *  报警历史记录 (供串口屏 Page 3 报警页/MQTT 历史查询使用)
 * ════════════════════════════════════════════════════════
 *
 * rule_engine 内部维护一个 16 条容量的环形缓冲, 每次触发新报警时
 * 写入一条记录。屏幕报警页通过 rule_engine_get_recent() 拉取最近 N 条
 * 用于显示, 顺序为"最新→最旧"。
 *
 * 缓冲满后新记录会覆盖最旧记录, 不阻塞业务路径。
 */

/** 报警等级 (与日志等级 + UI 颜色对应) */
typedef enum {
    ALARM_LEVEL_WARN  = 0,      /* 警告: ESP_LOGW, UI 黄色 */
    ALARM_LEVEL_FAULT = 1,      /* 故障: ESP_LOGE, UI 红色 + 触发 FSM 安全模式 */
} alarm_level_t;

/** 单条报警记录 (与 MQTT alert JSON 中的字段一一对应) */
typedef struct {
    time_t        timestamp;        /* Unix 时间戳 (秒). 未同步 SNTP 时为 0  */
    alarm_level_t level;            /* 警告/故障 */
    char          type[24];         /* 类型字符串, 如 "temp_warn"/"oxygen_high_3"/"sensor_offline" */
    float         value;            /* 实际值 */
    float         threshold;        /* 阈值 */
} alarm_record_t;

#define ALARM_HISTORY_CAPACITY  16  /* 环形缓冲容量 */

/**
 * @brief 初始化规则引擎
 *
 * 从 NVS 加载已保存的报警规则（阈值配置）。
 * 如果 NVS 中没有规则，则使用 app_config.h 中的默认值。
 *
 * @return ESP_OK 成功
 */
esp_err_t rule_engine_init(void);

/**
 * @brief 启动规则引擎服务
 *
 * 创建 FreeRTOS 任务 "rule_task"（优先级4），循环执行：
 *   1. 等待 EVT_SENSOR_DATA_READY 事件
 *   2. 读取最新传感器数据 sensor_service_get_data()
 *   3. 检查 data.valid → 无效则累加离线计数，触发传感器故障
 *   4. 氧浓度三级检查 → 执行器动作 + 语音 + MQTT
 *   5. 压力三级检查 → 执行器动作 + 语音 + MQTT
 *   6. 压力变化率检查 → 超限停机
 *   7. 严重异常：执行统一处理流程 → FSM 安全模式
 *
 * @return ESP_OK 任务创建成功
 */
esp_err_t rule_engine_start(void);

/**
 * @brief 拉取最近 N 条报警记录 (线程安全)
 *
 * 用于串口屏报警页 (Page 3) 显示历史报警。返回的顺序是 **最新在前**,
 * 即 out[0] 是最新的一条, out[count-1] 是最旧的一条。
 *
 * @param out   调用者分配的数组, 容量至少 max 个 alarm_record_t
 * @param max   out 数组容量 (建议 ≤ ALARM_HISTORY_CAPACITY)
 * @param count [out] 实际写入的条数 (0 ~ min(max, 当前缓冲条数))
 * @return ESP_OK 成功; ESP_ERR_INVALID_ARG out 或 count 为 NULL
 */
esp_err_t rule_engine_get_recent(alarm_record_t *out, uint8_t max, uint8_t *count);

#endif // RULE_ENGINE_H
